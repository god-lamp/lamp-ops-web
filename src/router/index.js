/**
 * 注意：子菜单仅在路由 children.length >= 1 时显示
 *
 * hidden: true                   如果设置为 true，则项目将不会显示在侧边栏中（默认为 false）
 * alwaysShow: true               如果设置为 true，将始终显示根菜单
 *                                如果不设置 alwaysShow，当项目有多个子路由时，它将变为嵌套模式，否则不显示根菜单
 * redirect: noRedirect           如果设置 noRedirect 将不会在痕迹导航中重定向
 * name:'router-name'             名称由<keep-alive>（必须设置!!）
 * meta : {
    roles: ['admin','editor']    控制页面角色（可以设置多个角色）
    title: 'title'               名称显示在侧边栏和面包屑中（推荐设置）
    icon: 'svg-name'/'el-icon-x' 图标显示在边栏中
    noCache: true                如果设置为 true，则不会缓存页面（默认值为 false）
    affix: true                  如果设置为 true，则标记将附加在 tags-view 中
    breadcrumb: false            如果设置为 false，则该项目将隐藏在面包屑中（默认值为 true）
    activeMenu: '/example/list'  如果设置了路径，侧边栏将突出显示您设置的路径
  }
 */

